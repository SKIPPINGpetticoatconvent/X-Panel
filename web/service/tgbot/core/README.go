// Package core 提供 Telegram Bot 的核心功能模块
//
// 这个包实现了 Telegram Bot 的模块化架构，包含以下主要组件：
//   - Context: 封装 bot 上下文和常用方法
//   - Router: 负责命令和回调的注册与分发
//   - Bot: 定义 Bot 生命周期管理接口
//
// 架构特点：
//   - 单一职责原则：每个组件都有明确的职责边界
//   - 依赖倒置：通过接口解耦具体实现
//   - 易于测试：模块化设计便于单元测试
//   - 可扩展性：支持动态注册新的命令和回调处理器
package core